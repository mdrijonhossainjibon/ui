export const EMAIL_PARTTERN = /^(?:[\w\!\#\$\%\&\'\*\+\-\/\=\?\^\`\{\|\}\~]+\.)*[\w\!\#\$\%\&\'\*\+\-\/\=\?\^\`\{\|\}\~]+@(?:(?:(?:[a-zA-Z0-9](?:[a-zA-Z0-9\-](?!\.)){0,61}[a-zA-Z0-9]?\.)+[a-zA-Z0-9](?:[a-zA-Z0-9\-](?!$)){0,61}[a-zA-Z0-9]?)|(?:\[(?:(?:[01]?\d{1,2}|2[0-4]\d|25[0-5])\.){3}(?:[01]?\d{1,2}|2[0-4]\d|25[0-5])\]))$/;
export const PASSWORD_PARTTERN = /^(?=.{8,})/;

export const isEmail = (email: string): boolean => {
	return EMAIL_PARTTERN.test(email);
};

export const isValidPassword = (pass: string): boolean => {
	return PASSWORD_PARTTERN.test(pass);
};
